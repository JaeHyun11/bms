CREATE TABLE MEMBER(
    MEMBER_ID 		VARCHAR(30) PRIMARY KEY,
	MEMBER_NM 		VARCHAR(50),
    PASSWD 			VARCHAR(1000),
	SEX 			VARCHAR(10),
	DATE_BIRTH 		VARCHAR(20),
	HP 				VARCHAR(20),
	SMSSTS_YN 		VARCHAR(20),
	EMAIL 			VARCHAR(70),
	EMAILSTS_YN 	VARCHAR(20),
	ZIPCODE 		VARCHAR(20),
	ROAD_ADDRESS 	VARCHAR(500),
	JIBUN_ADDRESS 	VARCHAR(500),
	NAMUJI_ADDRESS 	VARCHAR(500),
	POINT			INT,
	JOIN_DT 		DATETIME
);


CREATE TABLE GOODS(
	GOODS_NM		  VARCHAR(200),
    GOODS_CD 		  INT AUTO_INCREMENT PRIMARY KEY,
    GOODS_GROUP 	  VARCHAR(50),
	GOODS_CATEGORY	  VARCHAR(50),
    GOODS_COLOR		  VARCHAR(50),
    SORT			  VARCHAR(50),
	GOODS_INFO		  VARCHAR(2000),
	GOODS_DETAIL	  VARCHAR(2000),
	GOODS_FILE_NAME	  VARCHAR(2000),
	PRICE			  INT,
	DISCOUNT_RATE	  INT,
	XS_STOCK		  INT,
	SM_STOCK		  INT,
	MD_STOCK		  INT,
	LG_STOCK		  INT,
	XL_STOCK		  INT,
	POINT			  INT,
	DELIVERY_PRICE	  INT,
	ENROLL_DT 		  DATETIME
);


CREATE TABLE `ORDER`(
	ORDER_CD 			BIGINT AUTO_INCREMENT PRIMARY KEY,
    MEMBER_ID 			VARCHAR(20),
    GOODS_CD 			INT,
    SIZE_STOCK			VARCHAR(10),
	ORDER_GOODS_QTY 	INT,
	PAYMENT_AMT			INT,
    ORDERER_NM 			VARCHAR(50),
    ORDERER_HP 			VARCHAR(50),
	ZIPCODE 			VARCHAR(20),
	ROAD_ADDRESS 		VARCHAR(500),
	JIBUN_ADDRESS 		VARCHAR(500),
	NAMUJI_ADDRESS 		VARCHAR(500),
    DELIVERY_METHOD 	VARCHAR(40),
    DELIVERY_MESSAGE 	VARCHAR(300),
    DELIVERY_STATUS 	VARCHAR(100),
	GIFT_WRAPPING 		VARCHAR(20),
	PAY_METHOD 			VARCHAR(200),
	PAY_ORDERER_HP 		VARCHAR(50), 
	CARD_COMPANY_NM 	VARCHAR(50),
	CARD_PAY_MONTH 		VARCHAR(20),
    PAY_ORDER_TIME 		DATETIME
);


CREATE TABLE CART (
	CART_CD   	   BIGINT AUTO_INCREMENT PRIMARY KEY,
	MEMBER_ID 	   VARCHAR(20),
	GOODS_CD  	   INT,
	CART_GOODS_QTY INT,
	ENROLL_DT 	   DATETIME
);

CREATE TABLE WISHLIST (
	WISH_CD		  	INT,
	MEMBER_ID	 	VARCHAR(20),
	GOODS_CD	  	INT,
	WISH_GOODS_QTY	INT
);

CREATE TABLE QA (
	MEMBER_ID		VARCHAR(20),
	NUM				INT,
	EMAIL			VARCHAR(20),
	SUBJECT			VARCHAR(50),
	PW				VARCHAR(1000),
	REG_DATE		DATETIME,
	READ_COUNT		INT,
	CONTENT			VARCHAR(3000)
);

CREATE TABLE CONTACT (
	CONTACT_CD  INT AUTO_INCREMENT PRIMARY KEY,
	NAME		VARCHAR(50),
	EMAIL		VARCHAR(50),
	SUBJECT		VARCHAR(300),
	CONTENT		VARCHAR(3000),
	REG_DT  	DATETIME
);